"""
Выяснить тип результата выражений:

15 * 3
15 / 3
15 // 2
15 ** 2
"""

print(f'15 * 3  = {15 * 3}, тип результата выражения: {type(15 * 3)}')
print(f'15 / 3  = {15 / 3}, тип результата выражения: {type(15 / 3)}')
print(f'15 // 2  = {15 // 3}, тип результата выражения: {type(15 // 2)}')
print(f'15 ** 2  = {15 ** 2}, тип результата выражения: {type(15 ** 2)}')
